    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int respuesta= JOptionPane.showConfirmDialog(this, "Â¿Esta Seguro?");
        if (respuesta == JOptionPane.YES_OPTION){
            String identificaion=txtEliminarIdentificaion.getText();
            String nombre = txtEliminarNombre.getText();
            String correo =txtEliminarCorreo.getText();

            usuarioControlador.eliminarUsuario(identificaion);
            JOptionPane.showMessageDialog(this, "Usuario Eliminado Exitosamente");
            limpiarDatos();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarEliminarActionPerformed
        String identificaion = txtEliminarIdentificaion.getText();
        Usuario usuario = usuarioControlador.buscarUsuario(identificaion);

        if (usuario==null){
            JOptionPane.showMessageDialog(this, "Usuario No Encontrado");
        }else{
            txtEliminarIdentificaion.setEditable(false);
            txtEliminarCorreo.setText(usuario.getCorreo());
            txtEliminarCorreo.setEditable(true);
            txtEliminarNombre.setText(usuario.getNombre());
            txtEliminarNombre.setEditable(true);
        }
    }//GEN-LAST:event_btnBuscarEliminarActionPerformed

    private void btnCancelarEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarEliminarActionPerformed
        limpiarDatos();
    }//GEN-LAST:event_btnCancelarEliminarActionPerformed
    private void limpiarDatos(){
        txtEliminarCorreo.setEditable(false);
        txtEliminarNombre.setEditable(false);       
        txtEliminarIdentificaion.setEditable(true);
        txtEliminarIdentificaion.setText("");
        txtEliminarCorreo.setText("");
        txtEliminarNombre.setText("");        
    }
