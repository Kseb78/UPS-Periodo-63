    private void btnEliminarBibliotecaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarBibliotecaActionPerformed
        if (txtCodigoBiblioteca.getText().isBlank())
        {
            JOptionPane.showMessageDialog(this, "No ha ingresado un codigo");
        }else
        {
            int codigo = Integer.parseInt(txtCodigoBiblioteca.getText());
            Biblioteca biblioteca = bibliotecaControlador.read(codigo);
            if (biblioteca!=null)
            {
                txtNombreBiblioteca.setText(biblioteca.getNombre());
                txtDireccionBiblioteca.setText(biblioteca.getDireccion());
                txtTelefonoBiblioteca.setText(biblioteca.getTelefono()); 
                int i = JOptionPane.showConfirmDialog(null, "Â¿Esta seguro de eliminar la Biblioteca?");
                if(i==0)
                {
                    bibliotecaControlador.delete(codigo);
                    JOptionPane.showMessageDialog(this,"Se ha eliminado la Biblioteca");
                    limpiarDatos();
                }
                else if (i==1)
                {
                    JOptionPane.showMessageDialog(this,"No se ha eliminado la Biblioteca");
                }

            }else
                {
                    JOptionPane.showMessageDialog(this,"No existe una Biblioteca con tal codigo");
                }
            }
    }//GEN-LAST:event_btnEliminarBibliotecaActionPerformed

